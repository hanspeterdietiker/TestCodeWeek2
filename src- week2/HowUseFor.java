
public class HowUseFor {
	public static void main(String[] args) {
		
		// 1- Declarar uma variavel 2- Dar uma Condicao 3- Diminuir(--=-1) ou aumentar (++=+1) 
		
		for (int c = 15; c >= 1; c--) {
			
		//Neste caso ira diminuir(c--) o 15(int c) atÃ© o 1(c>=1)
			
			System.out.print(c + " ");
		}
	}
}
